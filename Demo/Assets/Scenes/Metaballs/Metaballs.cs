    void Polygonise(GridCell grid)
    {
       int cubeindex = 0;
       
       if (grid.g[0].val < 1.0f) cubeindex |= 1;
       if (grid.g[1].val < 1.0f) cubeindex |= 2;
       if (grid.g[2].val < 1.0f) cubeindex |= 4;
       if (grid.g[3].val < 1.0f) cubeindex |= 8;
       if (grid.g[4].val < 1.0f) cubeindex |= 16;
       if (grid.g[5].val < 1.0f) cubeindex |= 32;
       if (grid.g[6].val < 1.0f) cubeindex |= 64;
       if (grid.g[7].val < 1.0f) cubeindex |= 128;

       if (_EdgesTable[cubeindex] == 0)
          return;
       if ((_EdgesTable[cubeindex] & 1) != 0) _VertList[0] = VertexInterpolate(grid.g[0],grid.g[1]);
       if ((_EdgesTable[cubeindex] & 2) != 0) _VertList[1] = VertexInterpolate(grid.g[1],grid.g[2]);
       if ((_EdgesTable[cubeindex] & 4) != 0) _VertList[2] = VertexInterpolate(grid.g[2],grid.g[3]);
       if ((_EdgesTable[cubeindex] & 8) != 0) _VertList[3] = VertexInterpolate(grid.g[3],grid.g[0]);
       if ((_EdgesTable[cubeindex] & 16)!= 0) _VertList[4] = VertexInterpolate(grid.g[4],grid.g[5]);
       if ((_EdgesTable[cubeindex] & 32)!= 0) _VertList[5] = VertexInterpolate(grid.g[5],grid.g[6]);
       if ((_EdgesTable[cubeindex] & 64)!= 0) _VertList[6] = VertexInterpolate(grid.g[6],grid.g[7]);
       if ((_EdgesTable[cubeindex] & 128)!=0) _VertList[7] = VertexInterpolate(grid.g[7],grid.g[4]);
       if ((_EdgesTable[cubeindex] & 256)!=0) _VertList[8] = VertexInterpolate(grid.g[0],grid.g[4]);
       if ((_EdgesTable[cubeindex] & 512)!=0) _VertList[9] = VertexInterpolate(grid.g[1],grid.g[5]);
       if ((_EdgesTable[cubeindex] & 1024)!=0) _VertList[10] = VertexInterpolate(grid.g[2],grid.g[6]);
       if ((_EdgesTable[cubeindex] & 2048)!=0) _VertList[11] = VertexInterpolate(grid.g[3],grid.g[7]);

       for (int i=0 ; _TrianglesTable[cubeindex,i]!=-1 ; i+=3) 
       {
            if (_OutputTriangles.Count>=_MaxPolys-1)
                break;

            AddVertex(_VertList[_TrianglesTable[cubeindex,i  ]]);
            AddVertex(_VertList[_TrianglesTable[cubeindex,i+1]]);
            AddVertex(_VertList[_TrianglesTable[cubeindex,i+2]]);
       }
    }

