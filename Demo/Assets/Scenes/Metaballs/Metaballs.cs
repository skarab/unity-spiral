    Vector3 VertexInterpolate(MiniGridCell p1, MiniGridCell p2)
    {
       float mu;
 
       if (Mathf.Abs(1.0f-p1.val) < 0.00001f)
          return(p1.p);
       if (Mathf.Abs(1.0f-p2.val) < 0.00001f)
          return(p2.p);
       if (Mathf.Abs(p1.val-p2.val) < 0.00001f)
          return(p1.p);
       mu = (1.0f - p1.val) / (p2.val - p1.val);
       return p1.p + (p2.p - p1.p) * mu;
    }
        
